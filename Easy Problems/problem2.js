
<script type="text/javascript">
 var num1, num2, sum, diff, prod, quot;
 num1 = prompt("Enter the value of num1: ");
 num2 = prompt("Enter the value of num2: ");
 sum = parseInt(num1) + parseInt(num2);
 diff = parseInt(num1) - parseInt(num2);
 prod = parseInt(num1) * parseInt(num2);
 quot = parseInt(num1) / parseInt(num2);
 document.write("Sum is " + sum + "<br/>");
 document.write("Difference is " + diff + "<br/>");
 document.write("Product is " + prod + "<br/>");
 document.write("Quotient is " + quot + "<br/>");
</script>


